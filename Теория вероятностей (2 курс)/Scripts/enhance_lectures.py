CYR_EVENT = str.maketrans("АВСКН", "ABCKH")


def _lat(s: str) -> str:
    """Кириллические буквы событий → латиница в формулах."""
    return s.translate(CYR_EVENT).replace("⦁", "\\cdot ").replace("·", "\\cdot ")


def fix_math_notation(text: str) -> str:
    """Нормализация LaTeX: индексы ω, единые $...$, кириллица в P(...)."""

    # 1. Починить уже сломанное: $\omega$1 → $\omega_1$
    text = re.sub(r"\$\s*\\omega\s*\$\s*([0-9])", r"$\\omega_\1$", text)
    text = re.sub(r"\$\s*\\omega\s*\$\s*([0-9])", r"$\\omega_\1$", text)

    # 2. Сырые символы (если ещё не в math mode)
    text = re.sub(r"(?<!\$)ω([0-9])", r"$\\omega_\1$", text)
    text = re.sub(r"(?<!\$)Ω(?!\$)", r"$\\Omega$", text)

    # 3. Принадлежность и подмножества
    text = text.replace("$\\omega$∈$\\Omega$", "$\\omega \\in \\Omega$")
    text = text.replace(
        "Каждый элемент $\\omega$ множества $\\Omega$, то есть $\\omega$∈$\\Omega$",
        "Каждый элемент $\\omega$ множества $\\Omega$, то есть $\\omega \\in \\Omega$",
    )
    text = text.replace(
        "Любое событие А рассматривается как некоторое подмножество множества $\\Omega$, то есть А∈$\\Omega$",
        "Любое событие $A$ рассматривается как подмножество $\\Omega$, то есть $A \\subseteq \\Omega$",
    )

    # 4. $\omega_k$= текст → $\omega_k = \text{текст}$
    text = re.sub(
        r"\$\\omega_(\d+)\$\s*=\s*([^,$\n]+)",
        lambda m: f"$\\omega_{m.group(1)} = \\text{{{m.group(2).strip()}}}$",
        text,
    )

    # 5. Множества: $\Omega$={...} → одна формула
    def _fix_set(m: re.Match) -> str:
        inner = m.group(1).replace("$", "")
        indices = re.findall(r"\\omega_(\d+)", inner)
        if indices:
            items = ", ".join(f"\\omega_{i}" for i in indices)
            return f"$\\Omega = \\{{{items}\\}}$"
        return m.group(0)

    text = re.sub(r"\$\\Omega\$\s*=\s*\{([^}]+)\}", _fix_set, text)

    # 5. Склеенные доллары: $P(A+B)$=P(А)+P(В)
    def _glue_math(m: re.Match) -> str:
        return "$P(" + _lat(m.group(1)) + ")=" + _lat(m.group(2)) + "$"

    text = re.sub(r"\$P\(([^$]+)\)\$=([^$\n]+)", _glue_math, text)

    # 6. Строки-формулы с P(...) без обёртки
    def _wrap_prob_line(line: str) -> str:
        s = line.strip()
        if not s or s.startswith("#") or s.startswith("|") or s.startswith(">"):
            return line
        if s.startswith("$$") or (s.startswith("$") and s.endswith("$")):
            return line
        if re.search(r"P\s*\(", s) and re.search(r"[=+\-*/<>≠⦁]", s):
            body = _lat(s)
            body = body.replace("≠", "\\neq ")
            return f"${body}$"
        return line

    text = "\n".join(_wrap_prob_line(ln) for ln in text.splitlines())

    # 7. ($A+B$) → $A+B$
    text = re.sub(r"\(\$A\+B\$\)", r"$A+B$", text)

    # 8. P(А|Н1) → $P(A|H_1)$ (гипотезы)
    text = re.sub(
        r"P\(А\|Н([0-9])\)",
        r"$P(A|H_\1)$",
        text,
    )

    # 9. Убрать лишние пробелы внутри формул display
    text = re.sub(r"\$\$\s*\n\s*\\frac", r"$$\\frac", text)

    return text


def fix_notation(text: str) -> str:
    # НЕ заменять ω/Ω посимвольно — это ломает индексы (ω1 → $\omega$1)
    text = re.sub(r"Р\s*\(", "P(", text)
    text = re.sub(r"Р\s*\[", "P[", text)
    text = re.sub(r"\bР(?=[A-ZА-ЯH])", "P", text)
    text = re.sub(r"P\(Ā\)", "P(\\bar A)", text)
    text = text.replace("Ā", "$\\bar A$")
    text = re.sub(r"\bА\+В\b", "$A+B$", text)
    text = re.sub(r"\bАВ\b", "$AB$", text)
    text = re.sub(r"\bА-В\b", "$A-B$", text)
    text = text.replace("0≤ P(A) ≤1", "$0 \\le P(A) \\le 1$")
    text = re.sub(r"\n---\n\n## Всем спасибо!.*?(?=\n---|\Z)", "\n", text, flags=re.DOTALL)
    text = re.sub(r"\n## Всем спасибо!.*?(?=\n---|\Z)", "\n", text, flags=re.DOTALL)
    text = text.replace("ИЛИ\n\n????", "**Или** другое разбиение $\\Omega$ (по достоинству карт).")
    text = re.sub(r"\?\?\?\?\s*\n\n---", "", text)
    return text


def apply_section_replacements(num: int, text: str) -> str:
    if num not in SECTION_REPLACEMENTS:
        return text
    start, end, new = SECTION_REPLACEMENTS[num]
    i = text.find(start)
    j = text.find(end)
    if i == -1 or j == -1:
        return text
    j += len(end)
    return text[:i] + new + text[j:]


def dedupe_formulas(text: str) -> str:
    seen: set[str] = set()
    blocks = re.split(r"(\n---\n)", text)
    result = []
    for block in blocks:
        if block == "\n---\n":
            result.append(block)
            continue
        formulas = re.findall(r"\$\$.*?\$\$", block, flags=re.DOTALL)
        new_block = block
        for f in formulas:
            key = re.sub(r"\s+", " ", f.strip())
            if key in seen:
                new_block = new_block.replace(f, "", 1)
            else:
                seen.add(key)
        new_block = re.sub(r"\n{3,}", "\n\n", new_block)
        new_block = re.sub(r">\s*\*Часть содержимого — графические формулы в PPTX\.\*\s*\n?", "", new_block)
        result.append(new_block)
    return "".join(result)


def cleanup_artifacts(text: str) -> str:
    """Убрать артефакты конвертации PPTX."""
    lines = []
    for line in text.splitlines():
        s = line.strip()
        if s in {"m1", "m2", "m3", "Н1", "Н2", "Н3", "0,1", "0,4", "0,5", "0,98", "0,88", "0,92", "0,55", "0,45", "0,01", "0,02"}:
            continue
        if re.fullmatch(r"0[,.]\d+", s) and len(s) <= 5:
            continue
        lines.append(line)
    text = "\n".join(lines)

    text = re.sub(r"P\(\$A\+B\$\)", r"$P(A+B)$", text)
    text = re.sub(r"P\(\$AB\$\)", r"$P(AB)$", text)
    text = re.sub(r"\(\$A\+B\$\)", r"($A+B$)", text)
    text = re.sub(
        r"P\(\$A\+B\$\)=P\(А\)\+P\(В\)-P\(А⦁В\)",
        r"$$P(A+B)=P(A)+P(B)-P(AB)$$",
        text,
    )
    text = re.sub(
        r"P\(\$A\+B\$\)=P\(А\)\+P\(В\)",
        r"$$P(A+B)=P(A)+P(B)$$",
        text,
    )
    if "Следовательно," in text and "m_1+m_2" not in text:
        text = re.sub(
            r"Следовательно,\s*\n+\s*---",
            lambda _m: "Следовательно,\n\n$$P(A+B)=\\frac{m_1+m_2}{n}=P(A)+P(B)$$\n\n---",
            text,
            count=1,
        )
    text = re.sub(r"\$\$\s*P\(A\+B\)=\s*\n?\s*rac\{", "$$P(A+B)=\\frac{", text)
    text = re.sub(r"=\s*\n\s*rac\{", "=\\frac{", text)
    text = re.sub(
        r"## Теорема сложения совместных событий\n\nP\(\$A\+B\$\)=P\(А\)\+P\(В\)-P\(А⦁В\)\n\$A\+B\$",
        "## Теорема сложения совместных событий\n\n$$P(A+B)=P(A)+P(B)-P(AB)$$",
        text,
    )
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text


def format_tasks(text: str) -> str:
    text = re.sub(
        r"^Задача:\s*\n(.+?)(?=\n\n---|\n\n##|\Z)",
        lambda m: f"### Задача\n\n> {m.group(1).strip()}\n",
        text,
        flags=re.MULTILINE | re.DOTALL,
    )
    text = re.sub(
        r"^Решение:\s*\n",
        "\n**Решение:**\n\n",
        text,
        flags=re.MULTILINE,
    )
    return text


def format_comparison_table(text: str) -> str:
    old = """## Теория вероятностей

Абстрактное (теоретическое) описание действительности

Строит теоретическую вероятностную модель случайных явлений

Матстатистика

Работа  непосредственно с результатами  реальных экспериментов

Выполняется оценка значений неизвестных параметров с заданным уровнем достоверности

Выдвигается гипотеза (выводы по результатам наблюдений)

Происходит проверка гипотезы с заданной степенью точности"""
    new = """## Теория вероятностей vs Математическая статистика

| | **Теория вероятностей** | **Математическая статистика** |
|---|-------------------------|-------------------------------|
| Объект | Абстрактная модель | Реальные данные экспериментов |
| Задача | Построить вероятностную модель | Оценить параметры, проверить гипотезы |
| Направление | Модель → предсказания | Данные → выводы |"""
    return text.replace(old, new)


def enhance_lecture(num: int, text: str) -> str:
    text = fix_notation(text)
    text = apply_section_replacements(num, text)
    if num == 1:
        text = format_comparison_table(text)
    text = dedupe_formulas(text)
    text = cleanup_artifacts(text)
    text = fix_math_notation(text)
    text = format_tasks(text)

    for old, new in INSERTIONS.get(num, []):
        if old in text:
            text = text.replace(old, new, 1)

    # Убрать footer и старый Summary
    text = re.sub(r"\n---\n\n## Summary — что запомнить[\s\S]*?(?=\n---\n\n\*Источник|\Z)", "\n", text)
    text = re.sub(r"\n---\n\n\*Источник:.*", "\n", text, flags=re.DOTALL)
    text = text.rstrip()

    if num in SUMMARIES:
        if SUMMARIES[num] not in text:
            text += "\n\n---\n\n" + SUMMARIES[num]

    source = f"Теория вероятностей и математическая статистика Лекция {num}.pptx"
    text += f"\n\n---\n\n*Источник: `{source}`*"
    return text + "\n"


def main():
    for path in sorted(LECTURES.glob("лекция_*.md")):
        num = int(re.search(r"(\d+)", path.stem).group(1))
        enhanced = enhance_lecture(num, path.read_text(encoding="utf-8"))
        path.write_text(enhanced, encoding="utf-8")
        print(f"OK: {path.name}")


if __name__ == "__main__":
    main()
